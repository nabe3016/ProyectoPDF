    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
                JFileChooser filechooser = new JFileChooser();
        String ruta= "";
        try{
            if(filechooser.showSaveDialog(null)==filechooser.APPROVE_OPTION){
                ruta = filechooser.getSelectedFile().getAbsolutePath();
                 File archivo = new File(ruta);
                if(!new File(ruta).exists())
                {
                        BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
                        bw.write(textArea.getText().toString());
                        bw.close();
                        
                }else{
                if(JOptionPane.OK_OPTION == JOptionPane.showConfirmDialog(this,"El fichero existe,deseas reemplazarlo?",ruta,JOptionPane.YES_NO_OPTION));
                    {
                        BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
                        bw.write(textArea.getText().toString());
                        bw.close();
                    } 
                } 
            }
        }catch(Exception e){
            e.printStackTrace();
        }

    }//GEN-LAST:event_jMenuItem4ActionPerformed

