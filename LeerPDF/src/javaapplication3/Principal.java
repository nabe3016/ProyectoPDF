    private void jMenuItem8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem8ActionPerformed
       int a, e, i, o, u;
        a = e = i = o = u = 0;
        text = textArea.getText().toString();
        int longitud = text.length();
        int indice = 0;
        char letra;
        
        do {
            letra = text.charAt(indice);
            switch(letra) {
                   case 'a':
                   case 'á':
                       a++;
                       break;
                   case 'e':
                   case 'é':
                       e++;
                       break;
                   case 'i':
                   case 'í':
                       i++;
                       break;
                   case 'o':
                   case 'ó':
                       o++;
                       break;
                   case 'u':
                   case 'ú':
                       u++;
                       break;
                   default:
                       break;
            }

            indice++;
        }while(indice < longitud); 
        JOptionPane.showMessageDialog(null,"Catnidad de \n a="+a+"\n e="+e+"\n i="+i+"\n o="+o+"\n u="+u);

    }//GEN-LAST:event_jMenuItem8ActionPerformed

